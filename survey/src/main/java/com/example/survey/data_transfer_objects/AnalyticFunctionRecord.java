package com.example.survey.data_transfer_objects;

public class AnalyticFunctionRecord {
    private long func_id;
    private String func_name;
    private String func_path;

    public long getFunc_id() {
        return func_id;
    }

    public void setFunc_id(long func_id) {
        this.func_id = func_id;
    }

    public String getFunc_name() {
        return func_name;
    }

    public void setFunc_name(String func_name) {
        this.func_name = func_name;
    }

    public String getFunc_path() {
        return func_path;
    }

    public void setFunc_path(String func_path) {
        this.func_path = func_path;
    }

}
